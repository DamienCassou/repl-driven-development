# Write numbers 1..100 and replace multiples of 3 with “Fizz”, multiples of 5
#              with “Buzz”, and multiples of both with “FizzBuzz”.









































range(1, 100)









































# Huh, nothing's showing up with “C-x C-p”!
# Let's get the docs of “range” with “C-u C-u” …









































# Hey, look at that, the docs show exactly *how to use range*:
# It's a generator and so should be used with “list”.









































list(range(1, 100))









































# Huh, we're missing ‘100’!
# Take a look at the docs again...
# “range”'s second argument is excluded!
list(range(1, 101))









































# Let's use a “map” to get at each element
list(map(lambda i: i, range(1,101)))









































# Multiples of 3 ...
list(map(lambda i: 'Fizz'*(not i%3), range(1,101)))









































# Huh, we're seeing empty strings:  'Fizz' * 0 ≈ ''









































list(map(lambda i: 'Fizz'*(not i%3) or i, range(1,101)))









































# Multiples of 5...
list(map(lambda i: 'Fizz'*(not i%3) or 'Buzz'*(not i%5) or i, range(1,101)))









































# Huh, index 15 should be “FizzBuzz” not just “Fizz”!
# 15 is a multiple of both 3 and 5, so “or”/∨ is not appropriate, we should concat/“+” instead!
list(map(lambda i: 'Fizz'*(not i%3) + 'Buzz'*(not i%5) or i, range(1,101)))









































